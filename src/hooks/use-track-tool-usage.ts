export function useTrackToolUsage() {
  const pathname = usePathname()
  const { addRecentTool } = useUserPreferences()

  useEffect(() => {
    // Check if the current path is a tool page
    if (pathname && toolInfo[pathname]) {
      const { name, icon } = toolInfo[pathname]
      
      // Add to recent tools
      addRecentTool({
        name,
        href: pathname,
        icon
      })
      
      // Store in localStorage as a fallback
      try {
        // Get existing recent tools from localStorage
        const storedRecent = localStorage.getItem('recent-tools')
        let recentTools = storedRecent ? JSON.parse(storedRecent) : []
        
        // Remove if already exists
        recentTools = recentTools.filter((tool: any) => tool.href !== pathname)
        
        // Add to beginning and limit to 5 items
        recentTools = [{ name, href: pathname, icon }, ...recentTools].slice(0, 5)
        
        // Save back to localStorage
        localStorage.setItem('recent-tools', JSON.stringify(recentTools))
      } catch (error) {
        console.error('Error storing recent tools in localStorage:', error)
      }
    }
  }, [pathname, addRecentTool])
}